# Ввод сторон прямоугольника
a = float(input("Введите длину стороны A: "))
b = float(input("Введите длину стороны B: "))

# Расчёты
area = a * b
perimeter = 2 * (a + b)

# Вывод результата
print("Площадь прямоугольника:", area)
print("Периметр прямоугольника:", perimeter)

